/* STORED PROCEDURES */
USE PROCEDURES;

DELIMITER $

CREATE PROCEDURE NOME_EMPRESA()
BEGIN

    SELECT 'UNIVERSIDADE DOS DADOS' AS EMPRESA;

END
$

CALL NOME_EMPRESA()$

CREATE PROCEDURE SOMA()
BEGIN

    SELECT 10 + 10 AS SOMA;

END
$

CALL SOMA()$

DROP PROCEDURE SOMA$


CREATE PROCEDURE SOMA(NUMERO1 INT, NUMERO2 INT)
BEGIN

    SELECT NUMERO1 + NUMERO2 AS SOMA;

END
$

CALL SOMA(100, 50)$

DROP PROCEDURE SOMA$

DELIMITER ;




