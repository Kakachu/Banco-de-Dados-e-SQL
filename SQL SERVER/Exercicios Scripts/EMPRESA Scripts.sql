/* CLAUSULA AMBIGUA */

SELECT A.NOME,
	   ISNULL(T.TIPO, 'SEM') AS "TIPO",
	   ISNULL(T.NUMERO,'NUMERO') AS "TELEFONE",
	   E.BAIRRO,
	   E.UF
FROM ALUNO A
	LEFT JOIN TELEFONE T
	ON A.IDALUNO = T.ID_ALUNO
	INNER JOIN ENDERECO E
	ON A.IDALUNO = E.ID_ALUNO
GO

